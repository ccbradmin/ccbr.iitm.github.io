                   &nbsp <p style="margin-left: 75px; margin-top: 20px;">
   
                    The Winter course on Machine Intelligence and Brain Research (MIBR) at IIT Madras was held from Jan 2 - Jan 9th 2019. </p>
                   <p style="margin-left: 75px; margin-top: 20px;">We will upload the video lectures soon.</p> 
                    <h5 style="margin-left: 75px; margin-top: 20px;"><strong>Goals of the course </u></strong></h5>             
                   <p style="margin-left: 75px; margin-top: 20px;">Artificial neural network based machine learning architectures (software and hardware) are of great current interest in academia and industry. At the same time, owing to international brain initiatives, there are also rapid advances happening in Neuroscience. This interdisciplinary course is based on the premise that a two-way interchange between neuroscience and machine learning/AI will be mutually productive. The course focuses on four main areas of robust interactions between engineering and neuroscience (Vision, Speech/Audition, Natural Language, and Reinforcement Learning) and also includes related topics (classical Machine Learning, Hardware, Statistical Physics). </p> 
                  
                    <dl class=dl-horizontal"><dd style="margin-left: 75px; margin-top: 20px;"> 
                    <h4>The Center for Computational Brain Research (CCBR) at IIT Madras: </h4>
                     <p>The CCBR is an interdisciplinary center at IIT Madras focused on a two-way interchange between neuroscience and engineering. <!--This winter course follows on the annual workshops for CCBR of 2016 and 2017, details of which can be found here: <a  href="#" style="cursor: pointer;">https://ccbr.iitmadras.in/workshops</a>--></p>
                      
                      <p>The CCBR-IITM is comprised of faculty across several departments on campus and three visiting chair professors (Partha P Mitra, CSHL; Anand Raghunathan, Purdue; and Mriganka Sur, MIT). </p>
                      
                      <p> </p>
                      
                      <p>The workshop component of the course will consists of pedagogical lectures and tutorials by invited speakers, foster national and international collaborations in the subject area.  An outreach component of the workshop will include participants from the computer industry and medical research.</p>
                      
                   
                     
                    
                    
