mso-fareast-font-family:Arial;color:black'>4. Receptive Field Identification of
Visual Cortex Neurons<o:p></o:p></span></b></p>

<p class=MsoNormal style='text-align:justify;mso-hyphenate:none'><span
lang=EN-AU style='font-family:"Times New Roman",serif;mso-fareast-font-family:
"Droid Sans Fallback";mso-ansi-language:EN-AU'>Poonam Thapar, Mari Ganesh Kumar
M, Jilt Sebastian, Jacque Ip, Sami El-Boustani, Hema A. Murthy and Mriganka Sur
<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
lang=EN-AU style='font-family:"Times New Roman",serif;mso-fareast-font-family:
"Droid Sans Fallback";mso-ansi-language:EN-AU'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
lang=EN-AU style='font-family:"Times New Roman",serif;mso-fareast-font-family:
"Droid Sans Fallback";mso-ansi-language:EN-AU'>Effective modeling of
information mapping from the retina to neurons in the visual cortex and their
receptive field identification are crucial steps for understanding higher-level
visual processing. </span><span style='font-family:"Times New Roman",serif;
mso-fareast-font-family:Arial;color:black'>This work aimed to solve receptive
field identification using real data obtained from the visual cortex of mice.
The receptive fields of neurons corresponding to the input stimulus were
identified using a novel algorithm. A sparse noise visual stimulus consisting
of box positions in a rectangular array of 20 positions was fed as input, and
cortical responses to this stimulus were recorded simultaneously. Neuronal
responses for every position of the stimulus (box) on the screen were averaged
across the trials. The soma receptive field location of a single neuron was
similar across the frames, but needed to be aligned owing to experimental
artefacts such as movements. Normalized cross-correlation across trials was
employed to perform the template matching. Once the frames were aligned by this
method, a region of interest (ROI) was selected. The average response of the
single neuron was calculated across all the frames in the selected region of
interest, and used to determine the receptive field as the location where
similar responses were obtained across different trials.<o:p></o:p></span></p>

