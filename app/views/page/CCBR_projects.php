<p class=MsoNormal><b style='mso-bidi-font-weight:normal'><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:Arial;
color:black'>5. Automated Identification of Marmoset Calls<o:p></o:p></span></b></p>

<p class=MsoNormal><span style='font-family:"Times New Roman",serif;mso-fareast-font-family:
Arial;color:black'>Sakshi Verma, K. L. Prateek, Karthik Pandia, Nauman
Dawalatabad, Rogier Landman, Jitendra Sharma, Mriganka Sur and Hema A. Murthy<b
style='mso-bidi-font-weight:normal'><o:p></o:p></b></span></p>

<p class=MsoNormal><span style='font-family:"Times New Roman",serif;mso-fareast-font-family:
Arial;color:black'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Times New Roman",serif;
mso-fareast-font-family:Arial;color:black'>Marmosets are highly vocal primates.
Hence the calls made by them are extensively analyzed for understanding their
behavior. The first step for such studies is to identify and annotate the calls
in a marmoset conversation audio. A time-consuming way to annotate the calls is
to use human annotation. This process can be automated by using machines to
annotate the calls, similar to the task of annotating human speech. The
automatic annotation method can be either supervised or unsupervised.
Preliminary work has attempted to annotate marmoset calls using both the
methods above. The first step in automatic identification of calls is to
segment the calls. The segmentation can also be supervised or unsupervised. We
used a signal processing based segmentation technique to obtain the call
segments. These segments were used for both supervised and unsupervised call
identification methods. The primary aim of the experiment was to evaluate the
call detection performance. Hence, the segments obtained from the unsupervised
segmentation technique that matched the ground truth were chosen for
evaluation. The (semi) supervised method was a two-stage method. In the first
stage, a few templates for all the calls were hand-picked. Dynamic time warping
(DTW) was used to identify the calls in the segmented audio. To improve the
detection results from the first stage, the calls were reidentified using
hidden Markov models (HMMs), which were trained using the templates that gave
the best DTW scores. On example conversation files, call detection accuracies
of 77.6% and 81.4% were observed in the first and second stages respectively.
In unsupervised call identification, the segments were clustered automatically.
Initially, all the calls were identified as unique calls. An HMM-based
bottom-up clustering technique was applied to merge the calls, iteratively, to
obtain a final set of unique calls. An average cluster purity of 78.7% was
observed on two pairs of conversation data.<o:p></o:p></span></p>

