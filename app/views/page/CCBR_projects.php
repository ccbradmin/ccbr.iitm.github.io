<p class=Default><b style='mso-bidi-font-weight:normal'><span style='font-family:
"Times New Roman",serif'>5<i style='mso-bidi-font-style:normal'>) <span
style='mso-bidi-font-weight:bold'>ConnExt: Tool for Brain Region Connectivity
Extraction from Neuroscience research articles <o:p></o:p></span></i></span></b></p>

<p class=Default><b style='mso-bidi-font-weight:normal'><i style='mso-bidi-font-style:
normal'><span style='font-family:"Times New Roman",serif'><o:p>&nbsp;</o:p></span></i></b></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>Research on
brain region connectivity is of immense interest to neuroscientists.
Experiments done in the wet lab are often reported in scientific literature by
researchers from all over the world. In this work, we have attempted to compile
the evidences of connectivity between identified brain regions in the
literature and engineer a tool that can potentially be used by neuroscientists
to validate their experiments with what the community has reported. The tool
serves as a search engine for finding brain regions and their connections from
a large repository of around 55,000 full-text neuroscience articles. The tool
broadly uses Natural Language Processing and Machine Learning techniques. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>Several
connectivity extractor algorithms have been designed and packaged as part of
this tool. The algorithms range from simple regular expression pattern match,
to checking presence of connectivity words, and techniques using supervised
learning with different feature representations. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>The
supervised algorithms are based on methods by Ashika et al [1] which use
surface level and link parse based syntactic feature representation, trained on
the White Text corpus [2], which is a gold standard benchmark dataset,
consisting of 1377 abstracts from the Journal of Comparative Neurology. A brief
description of the algorithms used in the tool is given below. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>• RegExp patterns:
Set of popular connectivity patterns compiled by Gokdeniz et al [3] are applied
to the corpus, with the hypothesis that a sentence matching any of the regular
expressions contains a connected brain region pair. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>• Conn Word
presence: Presence of connectivity indicators such as the words <i>Afferent,
Connect, Innervate, Originate, Pathway, Project, Receive and Input </i>denote
connectivity between the brain region mentions in the sentence. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>• BoW
clustering: A supervised learning method using Bag-of-Word feature
representation. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>• Conn Word
clustering: A supervised learning method using binary valued feature
representation based on popular connectivity words. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>• LPBridge
clustering: A supervised learning method using features derived from link parse
based bridges. A bridge is the shortest path between two brain regions within
the link parse structure of the sentence. <o:p></o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'><o:p>&nbsp;</o:p></span></p>

<p class=Default><span style='font-family:"Times New Roman",serif'>Experiments
on the White Text corpus revealed that the RegExp patterns were able to recall
50% of the sentences with 70% precision, whereas the BoW clustering achieved a
recall of 91%, but with a limited precision of 29%. The Conn Word clustering
algorithm had a precision of 54% and recall of 58%, where as the LPBridge
clustering achieved the best balance between precision and recall at 41% and
85% respectively. <o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;line-height:
normal;mso-layout-grid-align:none;text-autospace:none'><span style='font-size:
12.0pt;font-family:"Times New Roman",serif'>The tool provides brain region
based search facility on the 55,000 article repository to find sentences
matching the queried region and also fetches connected brain region names as
stated in the articles. One gets to apply different algorithms to search the
repository and in case of the clustering algorithms, the confidence of the
retrieval is also mentioned with every search result. This facility is expected
