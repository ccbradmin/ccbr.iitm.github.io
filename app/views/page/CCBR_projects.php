<p class=MsoNormal style='text-align:justify;mso-hyphenate:none'><b
style='mso-bidi-font-weight:normal'><span style='font-family:"Times New Roman",serif;
mso-fareast-font-family:"Droid Sans Fallback"'>3. Modeling Neuronal Responses
Using Machine Learning <o:p></o:p></span></b></p>

<p class=MsoNormal style='text-align:justify;mso-hyphenate:none'><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:"Droid Sans Fallback"'>Sidharth
Bafna, Rajeev Vijay Rikhye, Mari Ganesh Kumar M, Hema A. Murthy and Mriganka
Sur<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-hyphenate:none'><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:"Droid Sans Fallback"'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:Arial;
color:black'>The primary visual cortex (V1) located on the occipital lobe is
one of the best studied cortical areas.<span style='mso-spacerun:yes'> 
</span>The objective of this work is to analyze and model V1 neuronal responses
using machine learning. Previously, deep networks have been used to capture the
visual processing capabilities of V1 using real-world data. The neuronal
signatures in V1 are well studied for simple stimuli such as gratings but not
for complex stimuli such as natural movies. In this work, we analyzed V1
recordings in response to natural movies, using data from several mice. </span><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:"Droid Sans Fallback"'>A
