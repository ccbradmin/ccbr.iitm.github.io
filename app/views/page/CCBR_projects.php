<p class=MsoNormal style='text-align:justify;mso-hyphenate:none'><span
lang=EN-AU style='font-family:"Times New Roman",serif;mso-fareast-font-family:
Verdana;mso-ansi-language:EN-AU'>Jilt Sebastian, Mari Ganesh Kumar, Mriganka
Sur and Hema A Murthy<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><b
style='mso-bidi-font-weight:normal'><span style='font-family:"Times New Roman",serif;
mso-fareast-font-family:Arial;color:black'><o:p>&nbsp;</o:p></span></b></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:Arial;
color:black'>Spike time estimation from calcium (Ca2+) fluorescence signals recorded
with large-scale imaging methods such two-photon microscopy is a fundamental
and challenging problem in neuroscience. Several models and algorithms have
been proposed for this task over the past decade; nevertheless, it is still
hard to achieve accurate spike timing estimates from Ca2+ fluorescence signals.
While most existing methods rely on the physiology of neurons for modeling the
spiking process, this work exploits the nature of the responses of indicators
to spikes using signal processing. Ca2+ indicators respond to a spike with a
sudden rise that is followed by an exponential decay. Exploiting this property
and the high resolution property of minimum phase group delay (GD) functions, a
technique for estimating the location of spikes, GDspike, is proposed. The Ca2+
signal is interpreted as the response of an impulse train to the Ca2+ trace (an
exponentially decaying function, where the decay rate varies based on the
indicator). The performance of the proposed algorithm is evaluated on nine datasets
which include various indicators, sampling rates and mouse brain regions. The
GDspike approach is compared with<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:Arial;
color:black'>the state-of-the-art signal processing algorithm MLspike, widely
used Vogelstein deconvolution algorithm (fast oopsi), and Spike Triggered Mixture
(STM) Model which is a state-of-the-art data<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
style='font-family:"Times New Roman",serif;mso-fareast-font-family:Arial;
color:black'>driven approach for spike estimation. The algorithm is evaluated
using three different metrics: the F-measure, the area under ROC (AUC), and the
correlation. The performance of GDSpike is superior to that of the standard
Vogelstein deconvolution algorithm and is comparable to that of the MLSpike
algorithm. GDSpike, being non-model based, can be used to post-process the
output of MLSpike, which further enhances performance.<o:p></o:p></span></p>

