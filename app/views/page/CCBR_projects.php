information by the brain. Functional characterization of this area aids further
studies on cortical computations. The existing state-of-the-art technique
identifies areas in mouse visual cortex using retinotopic maps, which are
responses mapped to stimulus location. Instead of using retinotopic maps, we
have explored statistical and machine learning methods for classifying cortical
areas from their response to different kinds of stimuli. Since each area has
its own characteristic responses to a particular stimulus, this response could
be learned using machine learning algorithms. Building such a model to identify
the cortical areas will also be helpful in identifying the properties of these
areas in processing visual information.<o:p></o:p></span></p>

