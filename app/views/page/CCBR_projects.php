<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-align:
justify;line-height:normal'><span style='font-size:12.0pt;font-family:"Times New Roman",serif'>Ms.
Sanchari Sen (Purdue)<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-align:
justify;line-height:normal'><span style='font-size:12.0pt;font-family:"Times New Roman",serif'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-align:
justify;line-height:normal'><span style='font-size:12.0pt;font-family:"Times New Roman",serif'>The
overarching goals of Prof. Anand Raghunathan's research are to improve the
efficiency of modern computing platforms by understanding and adopting
information processing principles from the brain. This is of particular
interest for the growing computing workloads presented by machine learning and
data analytics. While artificial intelligence has matched or surpassed natural
intelligence in many perceptual tasks, including image recognition, speech
recognition and natural language processing, there still exists a large energy
gap (three-to-four orders of magnitude) between computers running machine
learning algorithms and their biological counterparts. Thus, a long-term goal
of this research is to &quot;perform brain-like functions with closer to
brain-like efficiency&quot;. The scope of this research spans various layers of
the computing stack, from software to hardware architecture and circuits.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-bottom:0in;margin-bottom:.0001pt;text-align:
justify;line-height:normal'><span style='font-size:12.0pt;font-family:"Times New Roman",serif'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><b
style='mso-bidi-font-weight:normal'><span lang=EN-IN style='font-family:"Times New Roman",serif;
mso-fareast-font-family:Arial;color:black;mso-ansi-language:EN-IN'>1. Improving
the efficiency of neural networks through dynamic variable effort deep networks
(Sanjay Ganapathy, Swagath Venkataramani, Balaraman Ravindran, Anand
Raghunathan)<o:p></o:p></span></b></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
lang=EN-IN style='font-family:"Times New Roman",serif;mso-fareast-font-family:
Arial;color:black;mso-ansi-language:EN-IN;mso-bidi-font-weight:bold'>Deep
Neural Networks (DNNs) have advanced the state-of-the-art in a variety of
machine learning tasks and are deployed in increasing numbers of products and
services. However, the computational requirements of training and evaluating
large-scale DNNs are growing at a much faster pace than the capabilities of the
underlying hardware platforms that they are executed upon. We propose Dynamic
Variable Effort Deep Neural Networks (DyVEDeep) to reduce the computational
requirements of DNNs during inference. Previous efforts propose specialized
hardware implementations for DNNs, statically prune the network, or compress
the weights. Complementary to these approaches, DyVEDeep is a dynamic approach
that exploits the heterogeneity in the inputs to DNNs to improve their compute
efficiency with comparable classification accuracy. DyVEDeep augments DNNs with
dynamic effort mechanisms that, in the course of processing an input, identify
how critical a group of computations are to classify the input. DyVEDeep
dynamically focuses its compute effort only on the critical computa- tions,
while skipping or approximating the rest. We propose 3 effort knobs that
operate at different levels of granularity viz. neuron, feature and layer
levels. We build DyVEDeep versions for 5 popular image recognition benchmarks -
one for CIFAR-10 and four for ImageNet (AlexNet, OverFeat and VGG-16,
weight-compressed AlexNet). Across all benchmarks, DyVEDeep achieves 2.1x-2.6x
reduction in the number of scalar operations, which translates to 1.8x-2.3x
performance improvement over a Caffe-based implementation, with &lt; 0.5% loss
in accuracy.<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><b
style='mso-bidi-font-weight:normal'><span lang=EN-IN style='font-family:"Times New Roman",serif;
mso-fareast-font-family:Arial;color:black;mso-ansi-language:EN-IN'>2.
Fovea-inspired object detection in video (R. Athindran, Giridhur Sriraman,
Balaraman Ravindran, Anand Raghunathan)<o:p></o:p></span></b></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
lang=EN-IN style='font-family:"Times New Roman",serif;mso-fareast-font-family:
Arial;color:black;mso-ansi-language:EN-IN;mso-bidi-font-weight:bold'>Object tracking
in video is an important machine learning task with several applications
involving video summarization, search and autonomous driving. The computational
requirements of object tracking are dominated by the object detection step,
which processes each frame using a deep neural network and overlooks the
considerable temporal correlation between successive frames. We are developing
a technique that extracts &quot;foveal&quot; regions from a frame based on
object locations in the previous frame, and stitches the extracted regions into
a frame of smaller size that can be processed by a lower-complexity deep
network. A full input frame is processed periodically to detect new objects
that were not present in previous frames. Preliminary results on video datasets
suggest that the proposed technique is able to reduce the computational
requirements of object detection by ~30%, while maintaining competitive
accuracy.<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><b
style='mso-bidi-font-weight:normal'><span lang=EN-IN style='font-family:"Times New Roman",serif;
mso-fareast-font-family:Arial;color:black;mso-ansi-language:EN-IN'>3. An
event-driven processor for spiking neural networks (Arnab Roy, Swagath
Venkataramani, Neel Gala, Sanchari Sen, Kamakoti Veezhinathan, Anand
Raghunathan)<o:p></o:p></span></b></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
lang=EN-IN style='font-family:"Times New Roman",serif;mso-fareast-font-family:
Arial;color:black;mso-ansi-language:EN-IN;mso-bidi-font-weight:bold'>Spiking
neural networks (SNNs) are a class of neural network models that offer
increased realism in the simulation of biological neurons. From the machine
learning perspective, SNNs have the potential to reduce hardware complexity
since neuron outputs are only single bits (in hardware, this eliminates the
need for multipliers to perform synaptic weighting), and since they utilize
different, and potentially more efficient, information representations based on
time. However, evaluating large-scale SNNs (e.g., of the scale of the visual
cortex) on power-constrained systems requires significant improvements in
computing efficiency. A unique attribute of SNNs is their event-driven
nature-information is encoded as a series of spikes, and work is dynamically
generated as spikes propagate through the network. Therefore, parallel software
implementations of SNNs on multi-cores and graphics processing units (GPUs) are
severely limited by communication and synchronization overheads. Recent years
have seen great interest in deep learning accelerators, however, these
architectures are not well suited to the dynamic, irregular parallelism in
SNNs. We propose PEASE, a Programmable Event-driven processor Architecture for SNN
Evaluation. PEASE comprises of Spike Processing Units (SPUs) that are
dynamically scheduled to execute computations triggered by a spike.
Instructions to the SPUs are dynamically generated by Spike Schedulers (SSs)
that utilize event queues to track unprocessed spikes and identify neurons that
need to be evaluated. The memory hierarchy in PEASE is fully software managed,
and the processing elements are interconnected using a two-tiered bus-ring
topology matching the communication characteristics of SNNs. We propose a
method to map any given SNN to PEASE such that the workload is balanced across
SPUs and SPU clusters, while pipelining across layers of the network to improve
performance. We implemented PEASE at the RTL level and synthesized it to IBM 45
technology. Across 6 SNN benchmarks, our 64-SPU configuration of PEASE achieves
7.1×-17.5× and 2.6×-5.8× speedups, respectively, over software implementations
on an Intel Xeon E5-2680 CPU and NVIDIA Tesla K40C GPU. The energy reductions
over the CPU and GPU are 71×-179× and 198×-467×, respectively.<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify;mso-pagination:none'><b
style='mso-bidi-font-weight:normal'><span lang=EN-IN style='font-family:"Times New Roman",serif;
mso-fareast-font-family:Arial;color:black;mso-ansi-language:EN-IN'>4.
Exploiting sparsity to accelerate deep neural networks on general-purpose
processors (Vinod Ganesan, Neel Gala, Sanchari Sen, Kamakoti Veezhinathan,
Anand Raghunathan)<o:p></o:p></span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Times New Roman",serif;
color:black;mso-bidi-font-style:italic'>The computational demands posed by DNNs
have commonly been addressed through the design of hardware accelerators. To
achieve good performance, these accelerators employ large numbers of processing
elements and considerable on-chip memory, leading to an area and cost that is
prohibitive in applications such as low-cost wearable devices and IoT sensors.
Therefore, accelerating DNNs on these resource-constrained systems requires new
approaches. We propose lightweight extensions to improve the performance of
DNNs on General-Purpose Processors (GPPs) by exploiting </span><i
style='mso-bidi-font-style:normal'><span style='font-family:"Times New Roman",serif;
color:black'>sparsity</span></i><span style='font-family:"Times New Roman",serif;
color:black;mso-bidi-font-style:italic'> in different DNN data-structures,
viz., features, weights and backpropagated errors. Sparsity in DNNs can be both
static or dynamic depending on whether the zero values remain constant or vary
across different inputs to the network. Sparsity in weights, introduced by
pruning connections in the network after training, is static in nature. In
contrast, feature and error sparsities, caused by the thresholding nature of
the ReLU (Rectified Linear Unit) activation functions, are dynamic in nature.
Across 6 state-of-the-art image-recognition DNNs, dynamic sparsity results in
40-70% of the computations being rendered redundant, presenting a significant
opportunity for improving performance.<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-family:"Times New Roman",serif;
color:black;mso-bidi-font-style:italic'>We are exploring two complementary
directions to exploit sparsity. First, we proposed Sparsity-aware Core
Extensions, a set of ISA and micro-architectural extensions that enable the
dynamic detection of zero operands and skipping of future instructions that use
them. Our design ensures that the instructions to be skipped are prevented from
even being fetched, as squashing instructions after they have partially
executed incurs a penalty. The extensions incur just 1.04% area overhead over
an ARM Cortex A35 embedded processor. Across 6 state-of-the-art DNNs for both
training and inference, the proposed Sparsity-aware Core Extensions achieve
19%-31% reduction in execution time. Our ongoing work explores utilizing
sparsity to improve the efficiency of the memory hierarchy, by preventing zero
values from being loaded into or occupying parts of the cache. This involves
augmenting the cache with a special hardware structure that simply keeps track
of the addresses that contain zero values in a compact form, rather than store
the zeros explicitly in the cache.<o:p></o:p></span></p>

