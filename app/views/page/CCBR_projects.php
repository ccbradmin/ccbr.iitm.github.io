<p class=MsoNormal style='text-align:justify;mso-pagination:none'><span
lang=EN-IN style='font-family:"Times New Roman",serif;mso-fareast-font-family:
Arial;color:black;mso-ansi-language:EN-IN'>Orientation preference maps (OPMs) are
present in the cat, ferret and primates but are absent in rodents. In this
study we investigate the possible link between astrocytic<span
style='mso-spacerun:yes'>  </span>arbors and presence of OPMs. We simulate the
development of orientation maps with varying hypercolumn widths using a variant
of the Laterally Interconnected Synergetically Self-Organizing Map (LISSOM)
model, the Gain Control Adaptive Lateral (GCAL) model, with an additional layer
simulating the astrocytic activation. The synaptic activity of V1 neurons is
given as input to the astrocyte layer. The activity of this astrocyte layer is
now used to modulate the bidirectional plasticity of the lateral excitatory
connections in the V1 layer. By simply varying the radius of the astrocytes,
the extent of lateral excitatory neuronal connections can be manipulated. An
increase in the radius of lateral excitatory connections subsequently increases
the size of a single hypercolumn in the OPM. When these lateral excitatory
connections become small enough the OPM disappears and a salt-and-pepper
organization emerges.<o:p></o:p></span></p>

